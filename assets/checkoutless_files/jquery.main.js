// init custom hover
function initCustomHover(){
	var hoverClass = 'hover';
	var win = jQuery(window);
	var isTouchDevice = ('ontouchstart' in window) || window.DocumentTouch && document instanceof DocumentTouch;
	var isWinPhoneDevice = navigator.msPointerEnabled && /MSIE 10.*Touch/.test(navigator.userAgent);
	jQuery('.img-box, #mainArtshop').each(function(){
		var hold = jQuery(this);
		if ((isTouchDevice || isWinPhoneDevice) && (win.width()<700)) {
			var checkPosition = function(){
				var winTop = win.scrollTop();
				var winHeight = win.height();
				var holdTop = hold.offset().top;
				var holdHeight = hold.outerHeight(true);

				if (holdTop < winTop + winHeight - 70 && holdTop>winTop && holdTop + holdHeight > winTop) {
					hold.addClass(hoverClass);
				} else {
					hold.removeClass(hoverClass);
				}
			}
			win.bind('load resize scroll orientationchange',checkPosition);
		} else {
			hold.hover(function(){
				hold.addClass(hoverClass);
			},function(){
				hold.removeClass(hoverClass);
			})
		}
	})
}


