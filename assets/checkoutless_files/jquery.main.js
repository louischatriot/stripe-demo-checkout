// init drop links hover
function initDropLinks() {
    jQuery('#nav .active .drop-down').each(function() {
        var hold = jQuery(this);
        var targetBlock = hold.find('.tab-content');
        var links = hold.find('ul a');
        links.each(function() {
            var link = jQuery(this);
            var src = link.data('orig');
            var href = link.data('link');
            link.on('mouseenter', function() {
                targetBlock.html('<a href="' + href + '"><img src="' + src + '" /></a>');
            });
        });
    });
}

function initLinkHover() {
    var linksArray = jQuery("#nav .active .drop-down ul a");
    for (var i = 0; i < linksArray.length; i++) {
        var dataMedia = linksArray.eq(i).data('media');
        var src = linksArray.eq(i).data('src');
        var link = linksArray.eq(i).attr('href');
        var srcRetina = linksArray.eq(i).data('retina');
        if ((matchMedia && matchMedia(dataMedia).matches)) {
            linksArray.eq(i).data('orig', srcRetina);
        } else {
            linksArray.eq(i).data('orig', src);
        }
        linksArray.eq(i).data('link', link);
    }
}

function initScrollTo(element){
    jQuery(element).click(function(){
        var goscroll = false;
        var the_hash = jQuery(this).attr("href");
        var regex = new RegExp("\#(.*)","gi");
        var the_element = '';

        if(the_hash.match("\#(.+)")) {
            the_hash = the_hash.replace(regex,"$1");

            if(jQuery("#"+the_hash).length>0) {
                the_element = "#" + the_hash;
                goscroll = true;
            }
            else if(jQuery("a[name=" + the_hash + "]").length>0) {
                the_element = "a[name=" + the_hash + "]";
                goscroll = true;
            }

            if(goscroll) {
                jQuery('html, body').animate({
                    scrollTop:jQuery(the_element).offset().top
                }, 'fast');
                return false;
            }
        }
    });
};

// init slide menu
function initSlideMenu(){
	var animSpeed = 300;
	var activeClass = 'expanded-menu';
	var win = jQuery(window);
	var hold = jQuery('#wrapper');
	var opener = hold.find('.open-close .opener');
	var menu = hold.find('.open-close .slide');
	var li = menu.find('#navigation li');
	var li2 = jQuery('#nav li');
	var offset = menu.width();
	var slide = hold.find('#push');
	li.add(li2).on('click touchstart', function() {
		li.add(li2).removeClass('click')
		jQuery(this).addClass('click');
	});
	opener.on('click', function(e){
		if (hold.hasClass(activeClass)) {
			hold.removeClass(activeClass);
			slide.animate({marginLeft:0},{duration: animSpeed, queue: false})
		} else {
			hold.addClass(activeClass);
			slide.animate({marginLeft:offset},{duration: animSpeed, queue: false})
		}
		e.preventDefault();
	})
	function setStyles(){
		offset = menu.width();
		if (opener.is(':visible')) {
			slide.css({width: '100%'});
			if (hold.hasClass(activeClass)) {
				slide.css({marginLeft: offset})
			}
		} else {
			slide.removeAttr('style');
		}
	}
	win.bind('load resize orientationchange', setStyles);
}


