// lazy load plugin
;(function($){
	function LazyLoad(options){
		this.options = $.extend({
			filter: true,
			set: '.products',
			items: 'li',
			noMoreClass:'no-more',
			loadingClass: 'ajax-loading',
			hiddenLink:'a.hidden-loader',
			onInit: null,
			onSuccess: null
		},options);
		this.init();
	}
	LazyLoad.prototype = {
		init: function(){
			if (this.options.holder) {
				this.findElements();
				this.attachEvents();
				this.checkPosition();
				if (typeof this.options.onInit === 'function') this.options.onInit(this);
			}
		},
		findElements: function(){
			this.win = $(window);
			this.hold = $(this.options.holder);
			this.set = this.hold.find(this.options.set);
			this.hiddenLink = this.hold.find(this.options.hiddenLink);
			this.busy = false;
			this.delay = 50;
		},
		attachEvents: function(){
			var self = this;
			this.win.bind('scroll resize orientationchange', function(){
				self.checkPosition();
			})
		},
		checkPosition: function(){
			var self = this;
			if (this.win.height() + this.win.scrollTop() > this.hold.outerHeight() + this.hold.offset().top && this.hiddenLink.length && !this.busy){
				self.loadItems(self.hiddenLink.attr('href'));
			}
		},
		loadItems: function(href){
			var self = this;
			this.busy = true;
			this.hold.addClass(this.options.loadingClass);
			jQuery.ajax({
				url: href,
				type: 'get',
				data: 'ajax=1',
				dataType: 'html',
				success: function(msg){
					var data = jQuery(msg);
					var newItems = data[self.options.filter ? 'filter' : 'find'](self.options.items);
					newItems.appendTo(self.set);
					self.hiddenLink = data[self.options.filter ? 'filter' : 'find']('.hidden-loader');
					self.hold.removeClass(self.options.loadingClass);
					self.busy = false;
					
					if (typeof self.options.onSuccess === 'function') self.options.onSuccess(self, newItems);
					if (!self.hiddenLink.length) self.hold.addClass(self.options.noMoreClass);
					initPopups();
				},
				error: function(){
					alert('ajax error!')
				}
			})
		}
	};
	$.fn.lazyLoad = function(opt){
		return this.each(function(){
			$(this).data('LazyLoad', new LazyLoad($.extend(opt, {holder: this})));
		});
	};
}(jQuery));

